# Parametros end y sep

# parametro end -> Permite no generar un salto de linea y separarlo por lo que se indique, asi:
print("Esto es un", end=", ")
print("ejemplo")

# parametro sep -> Permite separar los valores en una impresion de pantalla(print) de acuedo a lo que se indique, asi:
print("1","2","3","4","5",sep="*")

