# Operadores aritmeticos

# Son:
"""
Suma(+) Resta(-) Multiplicacion(*)
Exponente(**) Modulo(%) Division(/)
"""

# Suma
print('Suma:')
numero_uno = 5
numero_dos = 4
resultado = numero_uno + numero_dos
print("La suma es = " + str(resultado) + '\n') # Convertir int a str

# Resta
print("Resta :")
numero_uno = 5
numero_dos = 4
resultado = numero_uno - numero_dos # Operacion aritmetica
print("La resta es = " + str(resultado) + '\n') # Convertir int a str

# Multiplicacion
print("Multiplicacion :")
numero_uno = 5
numero_dos = 4
resultado = numero_uno * numero_dos # Operacion aritmetica
print("La multiplicacion es = " + str(resultado) + '\n') # Convertir int a str

# Potenciacion
# ej: 2**5 = 2*2*2*2*2 = 32
print("Potenciacion :")
numero_uno = 2
numero_dos = 5
resultado = numero_uno ** numero_dos # Operacion aritmetica
print("La potenciacion es = " + str(resultado) + '\n') # Convertir int a str

# Modulo o resto -> es la cantidad que sobra despues de una division
# ej: 3/10 = 9 -> sobra 1
print("Modulo :")
numero_uno = 30
numero_dos = 8
resultado = numero_uno % numero_dos # Operacion aritmetica
print("El modulo es = " + str(resultado) + '\n') # Convertir int a str

# Division
print("Division :")
numero_uno = 5
numero_dos = 4
resultado = numero_uno / numero_dos # Operacion aritmetica
print("La division es = " + str(resultado) + '\n') # Convertir int a str

# Division Entera //
print("Division Entera:")
numero_uno = 5
numero_dos = 4
resultado = numero_uno // numero_dos # Operacion aritmetica
print("La division entera es = " + str(resultado) + '\n') # Convertir int a str
