# Ciclo For

"""
* for -> Es una estructura de control que nos permite repetir un bloque de instrucciones(sentencias), cierta cantidad de veces

         Sintaxis: for nombre_variable in objeto_iterable:            // Un objeto iterable es aquel que permite recorrer sus elementos uno a uno
                       instruccion
                       instruccion       
"""

#Ejemplo
string = "Hola"

for character in string:
    print(character)

print("\nFin del programa.")

print()

#Otro ejemplo
print("\nOtro ejemplo for: \n")
