# Funcion len()

"""
len() -> Permite obtener la longitud de una cadena de caracteres u objeto // comenzando desde el indice 0
"""
#Ej.
print(len("Hola"))

#Otro ejemplo
print("Hola tiene", len("Hola"), "caracteres.")

#Otro ejemplo
longitud = len("La Geekipedia")
print("La Geekipedia tiene", longitud, "caracteres.")
