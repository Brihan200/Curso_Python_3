# Listas

"""
* listas -> Se utilizan para almacenar conjuntos de informacion, de esta manera crear una coleccion de elementos organizados

            Sintaxis: lista = []
"""

#Ej -> lista homogenea(Tipo mismo de dato)
print("Lista Homogenea: ")
lista_homogenea = ["Javier", "Carlos", "Maria"]
print(lista_homogenea, "\n")

#Ej -> lista heterogenea(Diferente tipo de dato)
print("Lista Heterogenea: ")
lista_heterogenea = ["nombre", 3, 3.10, True]
print(lista_heterogenea, "\n\n")

#Otro ejemplo:
print("Otro ejemplo: \n")
print("* lista vacia: ")
lista_vacia = []
print(lista_vacia)

print("\n* lista homogenea: ")
vocales = ["a", "e", "i", "o", "u"]
print(vocales)

num_enteros = [1, 2, 3, 4, 5]
print(num_enteros)

num_decimales = [1.5, 2.2, 3.3, 4.9, 5.1]
print(num_decimales)

valores_bool = [True, False, False, True]
print(valores_bool)

print("\n* lista heterogenea: ")
datos = ['Carlos', 20, 1.70, True]
print(datos)


