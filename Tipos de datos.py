# Tipos de datos en python
"""
1. Enteros y largos
2. Flotantes
3. Numeros complejos
4. Cadenas
5. Booleanos
"""

# Enteros -> int - long
numero = 15
type(numero) # Indica el tipo de dato de la variable
print(numero, type(numero), '\n')

# Flotantes -> float
numero_flotante = 15.5
print(numero_flotante, type(numero_flotante), '\n')


# Complejos -> complex
numero_complejo = 5 + 6j
print(numero_complejo, type(numero_complejo), '\n')


# Cadenas  -> string
cadena = "Brihan Camilo Suarez Osorio"
print(cadena, type(cadena), '\n')


# Booleanos  -> bool
verdadero_falso = False
print(verdadero_falso, type(verdadero_falso), '\n')


